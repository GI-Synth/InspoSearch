export const SUPPORTED_LOCALES = ['en', 'fr', 'de', 'es', 'nl', 'it', ...Object.keys(generatedLocales)];

let _locale = 'en';

/** Detect the best matching supported locale from navigator.language */
function detectLocale() {
  try {
    const lang = (navigator.language || navigator.languages[0] || 'en').toLowerCase().split('-')[0];
    return SUPPORTED_LOCALES.includes(lang) ? lang : 'en';
  } catch (e) {
    return 'en';
  }
}

/** Initialise locale: load from localStorage, fallback to browser detection */
export function initLocale() {
  try {
    const saved = localStorage.getItem('insposearch_locale');
    if (saved && SUPPORTED_LOCALES.includes(saved)) {
      _locale = saved;
      return;
    }
  } catch (e) { /* ignore */ }
  _locale = detectLocale();
}

/** Get current locale code */
export function getLocale() {
  return _locale;
}

/** Set locale, persist to localStorage, re-apply DOM translations */
export function setLocale(lang) {
  if (!SUPPORTED_LOCALES.includes(lang)) return;
  _locale = lang;
  try { localStorage.setItem('insposearch_locale', lang); } catch (e) { /* ignore */ }
  // Update <html lang> attribute for accessibility
  try { document.documentElement.lang = lang; } catch (e) { /* ignore */ }
  applyI18n();
}

/**
 * Translate a key. Returns the English fallback if key not found in current locale.
 * @param {string} key  - translation key
 * @returns {string}
 */
export function t(key) {
  const locale = STRINGS[_locale] || STRINGS.en;
  return (key in locale) ? locale[key] : (STRINGS.en[key] || key);
}

/**
 * Walk the DOM and apply translations for:
 *   data-i18n="key"             → element.textContent
 *   data-i18n-placeholder="key" → element.placeholder
 *   data-i18n-title="key"       → element.title / aria-label
 *   data-i18n-aria="key"        → element.setAttribute('aria-label', ...)
 */
export function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
    const val = t(el.dataset.i18nTitle);
    el.title = val;
    if (el.hasAttribute('aria-label')) el.setAttribute('aria-label', val);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });
}
