#!/usr/bin/env node
/**
 * build-homepage-images.cjs
 *
 * Reads admin/data/homepage-candidates.json (the curated review file)
 * and outputs a compact insposearch/data/homepage-images.json containing
 * only the non-rejected image URLs, ready for the live app to consume.
 *
 * "available" = keep !== false  (not explicitly rejected)
 *
 * Usage:  node scripts/build-homepage-images.cjs
 */

const fs   = require('fs');
const path = require('path');

const INPUT  = path.join(__dirname, '..', 'admin',       'data', 'homepage-candidates.json');
const OUTPUT = path.join(__dirname, '..', 'insposearch', 'data', 'homepage-images.json');

// Map candidate group keys → CATEGORIES/HEROES keys used in app.js
const CAT_KEYS = [
  'paintings', 'photography', 'sculpture', 'architecture',
  'manuscripts', 'maps & charts', 'textiles', 'prints',
  'ceramics', 'natural history',
];
const HERO_KEYS = [
  'the dutch golden age',
  'japanese woodblock prints',
  'art nouveau & organic form',
  'illuminated manuscripts',
  'botanical illustration',
  'ancient maps & cartography',
  'impressionist light',
];

function pickURLs(items) {
  return items
    .filter(i => i.keep !== false && i.url)
    // Shuffle slightly using item index so same-source items are spread out
    .map((item, idx) => ({ item, sort: idx * 3 + (item.source === 'met' ? 0 : 1) }))
    .sort((a, b) => a.sort - b.sort)
    .map(({ item }) => item.url);
}

const raw  = fs.readFileSync(INPUT, 'utf-8');
const data = JSON.parse(raw);

const out = {
  _generated: new Date().toISOString(),
  _note: 'generated by scripts/build-homepage-images.cjs — do not edit by hand',
  categories: {},
  heroes: {},
};

console.log('\n  Building homepage-images.json\n');

for (const key of CAT_KEYS) {
  const items = data.categories[key] || [];
  out.categories[key] = pickURLs(items);
  console.log(`  ✓ ${key.padEnd(20)} ${out.categories[key].length} images`);
}

for (const key of HERO_KEYS) {
  const items = data.heroes[key] || [];
  out.heroes[key] = pickURLs(items);
  console.log(`  ✓ ${key.padEnd(30)} ${out.heroes[key].length} images`);
}

fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
fs.writeFileSync(OUTPUT, JSON.stringify(out, null, 2), 'utf-8');

const total = [...Object.values(out.categories), ...Object.values(out.heroes)]
  .reduce((s, a) => s + a.length, 0);

console.log(`\n  Total: ${total} images → insposearch/data/homepage-images.json\n`);
