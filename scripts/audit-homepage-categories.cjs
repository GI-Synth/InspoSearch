#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const dataDir = path.join(repoRoot, 'insposearch', 'data');
const outFile = path.join(repoRoot, 'homepage-category-search-audit.md');

function normalize(s) {
  return (s || '')
    .toString()
    .toLowerCase()
    .replace(/[&\/\\#,+()$~%.'\"*?<>{}:!@\[\]^_`|]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function readJson(fp) {
  try {
    const raw = fs.readFileSync(fp, 'utf8');
    return JSON.parse(raw);
  } catch (e) {
    console.error('Failed to parse', fp, e.message);
    return null;
  }
}

const homepagePath = path.join(dataDir, 'homepage-images.json');
const homepage = readJson(homepagePath);
if (!homepage || !homepage.categories) {
  console.error('homepage-images.json not found or contains no categories');
  process.exit(2);
}

const categories = Object.keys(homepage.categories);
const files = fs.readdirSync(dataDir).filter(f => f.endsWith('.json') && f !== 'homepage-images.json');

const stats = {};
categories.forEach(cat => {
  stats[cat] = { total: 0, tagMatches: 0, sources: new Set(), examples: [] };
});

files.forEach(fname => {
  const fp = path.join(dataDir, fname);
  const j = readJson(fp);
  if (!j || !Array.isArray(j.items)) return;
  const sourceId = j.sourceId || fname.replace(/\.json$/, '');

  j.items.forEach(item => {
    const titleRaw = item.title;
    const title = typeof titleRaw === 'string' ? titleRaw : (titleRaw && titleRaw['@id']) ? titleRaw['@id'] : JSON.stringify(titleRaw || '');
    const titleN = normalize(title);
    const tags = Array.isArray(item.tags) ? item.tags.map(t => normalize(t)) : [];
    const tagsText = tags.join(' ');

    categories.forEach(cat => {
      const catNorm = normalize(cat);
      const catWords = catNorm.split(' ').filter(Boolean);

      let matched = false;

      // match full normalized phrase in title
      if (catNorm && titleN.includes(catNorm)) matched = true;

      // match any meaningful word in title or tags
      if (!matched) {
        for (const w of catWords) {
          if (!w) continue;
          if (titleN.includes(w) || tagsText.includes(w)) { matched = true; break; }
        }
      }

      if (matched) {
        stats[cat].total += 1;
        stats[cat].sources.add(sourceId);
        // count as tagMatch if an exact tag equals any cat word
        for (const w of catWords) {
          if (tags.includes(w)) { stats[cat].tagMatches += 1; break; }
        }
        if (stats[cat].examples.length < 3) {
          stats[cat].examples.push({ title: title.trim(), source: sourceId });
        }
      }
    });
  });
});

// prepare summary and classification using quantiles
const counts = categories.map(c => stats[c].total).sort((a, b) => a - b);
const n = counts.length;
const lowCut = counts[Math.floor(n * 0.33)] || 0;
const highCut = counts[Math.floor(n * 0.66)] || 0;

function classify(count) {
  if (count <= lowCut) return 'low';
  if (count >= highCut) return 'high';
  return 'moderate';
}

const date = new Date().toISOString();

let md = `# Homepage Category Search Audit\n\n`;
md += `Date: ${date}\n\n`;
md += `Scanned local data files: ${files.length}\n\n`;

const high = [];
const moderate = [];
const low = [];

categories.forEach(cat => {
  const s = stats[cat];
  const count = s.total;
  const sourceCount = s.sources.size;
  const cls = classify(count);
  const entry = { cat, count, sourceCount, examples: s.examples };
  if (cls === 'high') high.push(entry);
  else if (cls === 'low') low.push(entry);
  else moderate.push(entry);
});

function renderList(arr) {
  if (arr.length === 0) return 'None\n\n';
  return arr.map(e => {
    const ex = e.examples.map(x => `"${x.title}" (${x.source})`).join('; ');
    return `- **${e.cat}**: ${e.count} matches across ${e.sourceCount} sources. Examples: ${ex}`;
  }).join('\n') + '\n\n';
}

md += `## Lots of results (top third)\n\n`;
md += renderList(high);

md += `## Moderate results (middle third)\n\n`;
md += renderList(moderate);

md += `## Few / Low results (bottom third)\n\n`;
md += renderList(low);

md += `---\nGenerated by scripts/audit-homepage-categories.cjs\n`;

fs.writeFileSync(outFile, md, 'utf8');
console.log('Wrote', outFile);
